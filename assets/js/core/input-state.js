export const INPUT_MODE = Object.freeze({
  IDLE: 'idle',
  CAMERA_PAN: 'camera-pan',
  FURNITURE_SELECTED: 'furniture-selected',
  FURNITURE_DRAG: 'furniture-drag',
  PINCH_ZOOM: 'pinch-zoom',
  CAT_INTERACTION: 'cat-interaction',
  CARE_INTERACTION: 'care-interaction',
  UI_BLOCKED: 'ui-blocked'
});

const TRANSITIONS = Object.freeze({
  [INPUT_MODE.IDLE]: new Set([INPUT_MODE.CAMERA_PAN, INPUT_MODE.FURNITURE_SELECTED, INPUT_MODE.PINCH_ZOOM, INPUT_MODE.CAT_INTERACTION, INPUT_MODE.CARE_INTERACTION, INPUT_MODE.UI_BLOCKED]),
  [INPUT_MODE.CAMERA_PAN]: new Set([INPUT_MODE.IDLE, INPUT_MODE.PINCH_ZOOM, INPUT_MODE.UI_BLOCKED]),
  [INPUT_MODE.FURNITURE_SELECTED]: new Set([INPUT_MODE.IDLE, INPUT_MODE.CAMERA_PAN, INPUT_MODE.FURNITURE_DRAG, INPUT_MODE.PINCH_ZOOM, INPUT_MODE.CAT_INTERACTION, INPUT_MODE.CARE_INTERACTION, INPUT_MODE.UI_BLOCKED]),
  [INPUT_MODE.FURNITURE_DRAG]: new Set([INPUT_MODE.FURNITURE_SELECTED, INPUT_MODE.IDLE, INPUT_MODE.PINCH_ZOOM]),
  [INPUT_MODE.PINCH_ZOOM]: new Set([INPUT_MODE.IDLE, INPUT_MODE.FURNITURE_SELECTED]),
  [INPUT_MODE.CAT_INTERACTION]: new Set([INPUT_MODE.IDLE, INPUT_MODE.FURNITURE_SELECTED, INPUT_MODE.PINCH_ZOOM, INPUT_MODE.CARE_INTERACTION]),
  [INPUT_MODE.CARE_INTERACTION]: new Set([INPUT_MODE.IDLE, INPUT_MODE.FURNITURE_SELECTED]),
  [INPUT_MODE.UI_BLOCKED]: new Set([INPUT_MODE.IDLE, INPUT_MODE.FURNITURE_SELECTED])
});

export function canTransitionInputMode(from, to) {
  return from === to || Boolean(TRANSITIONS[from]?.has(to));
}

export function stableInputMode({selectedItemId = null} = {}) {
  return selectedItemId ? INPUT_MODE.FURNITURE_SELECTED : INPUT_MODE.IDLE;
}
